package com.nly.test;

public class StudentGrand {
  private String id;
  private String name;
  private String keche;
  private String fenshu;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getKeche() {
	return keche;
}
public void setKeche(String keche) {
	this.keche = keche;
}
public String getFenshu() {
	return fenshu;
}
public void setFenshu(String fenshu) {
	this.fenshu = fenshu;
}
public StudentGrand(String id, String name, String keche, String fenshu) {
	super();
	this.id = id;
	this.name = name;
	this.keche = keche;
	this.fenshu = fenshu;
}
  
  
}
